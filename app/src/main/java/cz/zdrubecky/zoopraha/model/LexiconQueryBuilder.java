package cz.zdrubecky.zoopraha.model;

public class LexiconQueryBuilder {
    private String mBiotope;
    private String mClassName;
    private String mContinents;
    private String mDescription;
    private String mDistribution;
    private String mFood;
    private String mLimit;
    private String mLocation;
    private String mName;
    private String mOrderName;
    private String mOffset;

    public String getBiotope() {
        return mBiotope;
    }

    public void setBiotope(String biotope) {
        mBiotope = biotope;
    }

    public String getClassName() {
        return mClassName;
    }

    public void setClassName(String className) {
        mClassName = className;
    }

    public String getContinents() {
        return mContinents;
    }

    public void setContinents(String continents) {
        mContinents = continents;
    }

    public String getDescription() {
        return mDescription;
    }

    public void setDescription(String description) {
        mDescription = description;
    }

    public String getDistribution() {
        return mDistribution;
    }

    public void setDistribution(String distribution) {
        mDistribution = distribution;
    }

    public String getFood() {
        return mFood;
    }

    public void setFood(String food) {
        mFood = food;
    }

    public String getLimit() {
        return mLimit;
    }

    public void setLimit(String limit) {
        mLimit = limit;
    }

    public String getLocation() {
        return mLocation;
    }

    public void setLocation(String location) {
        mLocation = location;
    }

    public String getName() {
        return mName;
    }

    public void setName(String name) {
        mName = name;
    }

    public String getOrderName() {
        return mOrderName;
    }

    public void setOrderName(String orderName) {
        mOrderName = orderName;
    }

    public String getOffset() {
        return mOffset;
    }

    public void setOffset(String offset) {
        mOffset = offset;
    }
}
